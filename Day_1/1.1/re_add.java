import java.util.*;

public class re_add {
    static int adds(int a,int b){
        return a + b;
    }
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        int i = sc.nextInt();
        int j = sc.nextInt();
        System.out.println(adds(i,j));
    }
    
}
