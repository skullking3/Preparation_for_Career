public class data_types{
    public static void main(String[] args){
        //number containers
        //The byte data type can store whole numbers from -128 to 127
        byte s=12;
        System.out.println(s);
        //The int data type can store whole numbers from -2147483648 to 2147483647
        int i=54564;
        System.out.println(i);
        //The short data type can store whole numbers from -32768 to 32767
        short j=120;
        System.out.println(j);
        //The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807
        long a=15202;
        System.out.println(a);
        //The float and double data types can store fractional numbers
        float k=52.61f;
        System.out.println(k);
        //double
        double l=51.364d;
        System.out.println(l);
        //boolean data type that can only have one of two values, like:true,flase.
        boolean d=true;
        System.out.println(d);
        // The char data type is used to store a single character.
        char r='a';
        System.out.println(r);
        //The String data type is used to store a sequence of characters (text)
        String q="lord krishna";
        System.out.println(q);
        //Literals are the constant values that are assigned to the variables. 
        //Literals represent fixed values that cannot be modified. 
        String name=null;
        System.out.println(name);

    }
}