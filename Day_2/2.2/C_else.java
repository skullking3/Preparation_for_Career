public class C_else{
    public static void main(String[] args) {
        int i = 3;
        if(i<3){
            System.out.println(i+"If run");
        }
        else if(i<30){
            System.out.println(i+"else-if run");
        }
        else{
            System.out.println(i+"else run");
        }
    }
}