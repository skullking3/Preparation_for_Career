public class two_d_ary {
    public static void main(String[] args) {
        int[][] a ={
            {654,52,985},
            {74,71,72}
        }; 
        
        for(int i=0;i<=a.length;i++){
            for(int j=0;j<=a.length;j++){
                System.out.print(a[i][j]+" ");
            }System.out.println(" ");
        }
    }
    
}
